// Tạo một biến có kiểu dữ liệu là string và gán giá trị cho nó.
let myname: String = "Dinhpv";
console.log("My name is ", myname);

//Tạo một biến có kiểu dữ liệu là number và gán giá trị cho nó.
let age:Number = 18
console.log("My age is ", age, "years old");

//Tạo một biến có kiểu dữ liệu là boolean và gán giá trị cho nó.
let male:boolean = true;
if(male){
    console.log("My sex is Male")
}
else{
    console.log("My sex is Female")
}
